			<h3>Collar</h3>
<p>This is a method for packing that prevents the contact of packing materials with a painting's surface. At Ship/Art we use strips of 3/16” foamcore cut to the length of each section of the frame joined together on the back and then folded forward around the sides of the frame or canvas. This creates a “wall” that protects beyond the paintings surface so that the piece can be wrapped with the poly held well away from the surface of the work.</p>
<h3>Slip Case</h3>
<p>Although not a slip case in the strict sense, in the fine arts service industry this term has come to mean enclosing a two dimensional object in a layer of cardboard. This custom box adds another layer of protection to work usually wrapped in our custom manufactured virgin polyethylene sheeting. Sometimes a layer of cushioning material like bubble wrap or “microfoam” maybe included inside the slip case if required.  This method provides enough protection that many museums utilize it for one-way packing on art service shuttles. </p>

<h3>Cavity Pack</h3>
<p>This method consists of cutting object-shaped compartments in foam. It is frequently used in traveling exhibition packing because of its high level of object visibility and for its ease of repacking. When urethane foam is used it will tend to mitigate vibration and stabilize temperature and humidity. When Polyethylene foam along with other neutral materials is used containers can also double as storage mounts.</p>

<h3>Stretch Wrap</h3>
<p>The use of this material for holding objects (usually in combination with HDPE) was first established during Smithsonian move projects at the beginning of this century. Used for packing the most fragile objects imaginable, it provides an unparalleled quality of contact. Unlike foam it automatically conforms to the shape of the object being packed and provides smoother contact than any foam. </p>

<h3>Tissue Bubble Float</h3>
<p>This well established and widely used packing method has been responsible for the safe transportation of a vast number of fragile 3-D objects. Pillows and coils of soft tissue are used to closely surround each object. Strategically placed to protect any vulnerable projections, the tissue is then encased with by a layer of bubble wrap to form a soft package. These packages are then placed in a foam-lined cardboard box separated and secured in place with dunnage. Traditionally, dunnage consists of pillows formed from newsprint, but other dunnage materials are also available. Options include pieces of soft foam or Styrofoam peanuts sealed in bags to eliminate shifting and facilitate safe packing.</p>

<h3>Inner Boxes</h3>
<p>These typically lightweight containers whether constructed of cardboard, foamcore, blueboard or Coroplast not only provide protection during transit, they also provide support to objects allowing them to be handled without direct contact during the installation process within the gallery setting. They also create an improved sub-environment by allowing objects to be wrapped and sealed in poly without being handled directly. Inner boxes provide consistent surfaces for more accurate cushioning calculation.</p>


