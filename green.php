<p>Ship/Art has developed an extensive environmental program at both our San Francisco and Denver offices. We have developed our program after looking at length at all aspects of our business. From the trucks we drive to the lights in our warehouse, we are proud of the progress we have made and that others in the industry have used us as a model in developing their programs.</p>

<p>As part of our commitment towards environmental sustainability we are constantly looking at new ways of reducing our effect on the environment. Ship/Art understands that environmental sustainability is a global issue that requires a global effort to have any effect. No single person, company, or museum can do this alone. It will take all of us! Ship/Art as well as the entire museum community is working very hard on doing their part to reduce their effect on the environment. </p>

<p>Some of the efforts included in Ship/Art’s environmental program are:</p>

<ul>
<li>Using bio diesel fuels in all of our trucks.</li>
<li>Recycling all polyethylene and bubble sheeting, foam, and cardboard.</li>
<li>Reusing our wooden crates whenever possible, or recycling crates into pellets that fuel a power plant.</li>
<li>Buying recycled materials or sustainable harvested woods.</li>
<li>Planting a tree for every crate that is purchased – this is in partnership with the Nature Conservancy to help restore a crucial rainforest and ecosystem in Brazil.</li>
<li>Using 100% recycled paper in our offices.</li>
</ul>

<p>To create a larger impact, Ship/Art has teamed up with our friends at ICEFAT (International Convention of Fine Art Transporters). With 79 companies in 34 countries ICEFAT represents the best of the best when it comes to art transportation throughout the world. ICEFAT is also committed to environmental sustainability and has set up a committee to develop environmental standards throughout the world for our type of industry. We are currently doing a survey of all the ICEFAT companies to determine what efforts might be possible around the globe. It is our hope that with the help of the museums and art transportation companies throughout the world that we can make a difference and set an example for other industries to follow!</p>

<p>Please feel free to contact us if you want further information on this subject.</p>
