			<h3>Introduction</h3>
			<p>The Ship Art Vault facility located in Livermore, California is the product of years of research and development into creating the best possible Fine Arts Storage Facility in the San Francisco Bay Area. It is a direct response to input from private clients, museums and the insurance industry as a way of diversifying risk in a world that is seeing an increase in catastrophic events such as fires, earthquakes, hurricanes and floods. The San Francisco Bay Area is home to some of the
			most amazing art collections but this area does come with significant risks. Due to its location, The Livermore Vault facility is designed to directly reduce those risks as well as allow some geographic risk diversification.</p>
