			<h3>San Francisco and Peninsula</h3>
			<p>Companies located in San Francisco or the Peninsula are at risk from the San Andreas fault line. Based on reports by the
			USGS this fault line has a probability rating of 21% of an earthquake of 6.7 or greater during the next 14 years.</p>

			<h3>Oakland Area</h3>
			<p>Companies located in the Oakland area are at risk from the Hayward fault line. Based on reports by the USGS this fault line has a probability rating of 27% of an earthquake of 6.7 or greater during the next 14 years.</p>

			<p>Companies located in the Oakland area are at risk from the Greenville fault line. Based on reports by the USGS this fault line has a probability rating of 3% of an earthquake of 6.7 or greater during the next 14 years.</p>

      <h3>Livermore Area</h3>

			<p>The Ship Art Vault located in Livermore is the only Museum Level Fine Arts Storage Facility in the San Francisco greater Bay Area that has an earthquake probability rating of less than 5%. All other facilities including Ship Art headquarters have a probability rating of 21% or above in the next 14 years.</p>

			<h3>Flood Risk Assessment</h3>
			<p>The Ship Art Livermore Vault facility lies in a FEMA Flood Zone X. This is one of FEMA's lowest probability zones. It is outside of even the 500 year flood zone.</p>
			<img src="images/livermore1.jpg">
			<p>Summary: There is very little to no risk of flood at this location.</p>

			<h3>Liquefaction Risk Assessment</h3>
			<h4>Risk Assessment:</h4>
			<p>All Museum level Fine Art Storage Facilities in the San Francisco Bay Area are at moderate risk for Liquefaction, as per the Association of Bay Area Governments and includes the ShipArt Livermore Vault Facility.</p>
			<h4>What is Liquefaction?</h4>
			<p>Earthquake liquefaction, often referred to simply as liquefaction, is the process by which saturated, unconsolidated soil or sand is converted into a suspension during an earthquake. The effect on structures and buildings can be devastating, and is a major contributor to urban seismic risk.</p>
